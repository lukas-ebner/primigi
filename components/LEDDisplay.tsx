function textToPixels(text: string): number[] {
  const cols: number[] = [];
  for (const ch of text) {
    const glyph = FONT[ch] || FONT[ch.toUpperCase()] || FONT["?"];
    for (const col of glyph) cols.push(col);
    cols.push(0); // 1px spacing
  }
  return cols;
}

interface LEDDisplayProps {
  text: string;
  rows?: number;
  visibleCols?: number;
}

export default function LEDDisplay({
  text,
  rows = 7,
  visibleCols = 32,
}: LEDDisplayProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const offsetRef = useRef(0);
  const pixelColsRef = useRef<number[]>([]);
  const animRef = useRef<number | null>(null);

  useEffect(() => {
    const t = text.trim() || "PRIMIGI";
    pixelColsRef.current = textToPixels(t);
    const padding = Array(visibleCols).fill(0);
    pixelColsRef.current = [
      ...padding,
      ...pixelColsRef.current,
      ...padding,
    ];
    offsetRef.current = 0;
  }, [text, visibleCols]);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const dotSize = 8;
    const gap = 3;
    const cellSize = dotSize + gap;
    canvas.width = visibleCols * cellSize;
    canvas.height = rows * cellSize;

    let lastTime = 0;
    const speed = 60;

    const draw = (timestamp: number) => {
      if (timestamp - lastTime > speed) {
        lastTime = timestamp;
        offsetRef.current++;
        if (offsetRef.current >= pixelColsRef.current.length)
          offsetRef.current = 0;
      }

      ctx.fillStyle = "#0a0a12";
      ctx.fillRect(0, 0, canvas.width, canvas.height);

      for (let cx = 0; cx < visibleCols; cx++) {
        const colIdx =
          (offsetRef.current + cx) % pixelColsRef.current.length;
        const colData = pixelColsRef.current[colIdx] || 0;
        for (let ry = 0; ry < rows; ry++) {
          const isOn = (colData >> ry) & 1;
          const x = cx * cellSize;
          const y = ry * cellSize;
          if (isOn) {
            ctx.shadowColor = "#38bdf8";
            ctx.shadowBlur = 10;
            ctx.fillStyle = "#38bdf8";
            ctx.beginPath();
            ctx.arc(
              x + dotSize / 2,
              y + dotSize / 2,
              dotSize / 2,
              0,
              Math.PI * 2
            );
            ctx.fill();
            ctx.shadowBlur = 0;
          } else {
            ctx.fillStyle = "rgba(56, 189, 248, 0.06)";
            ctx.beginPath();
            ctx.arc(
              x + dotSize / 2,
              y + dotSize / 2,
              dotSize / 2,
              0,
              Math.PI * 2
            );
            ctx.fill();
          }
        }
      }
      animRef.current = requestAnimationFrame(draw);
    };

    animRef.current = requestAnimationFrame(draw);
    return () => {
      if (animRef.current !== null) cancelAnimationFrame(animRef.current);
    };
  }, [rows, visibleCols]);

  return (
    <canvas
      ref={canvasRef}
      style={{
        width: "100%",
        maxWidth: 640,
        height: "auto",
        aspectRatio: `${visibleCols * 11} / ${rows * 11}`,
        borderRadius: 12,
        imageRendering: "pixelated",
      }}
    />
  );
}
